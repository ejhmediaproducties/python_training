# Voorbeeldopdracht voor variabelen en berekeningen
nummer1 = int(input("Voer een getal in: "))
nummer2 = int(input("Voer nog een getal in: "))

som = nummer1 + nummer2
verschil = nummer1 - nummer2
product = nummer1 * nummer2
quotient = nummer1 / nummer2

print("De som is:", som)
print("Het verschil is:", verschil)
print("Het product is:", product)
print("Het quotient is:", quotient)


